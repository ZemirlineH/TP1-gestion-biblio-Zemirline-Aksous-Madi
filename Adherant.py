class Adherant:

    def __init__(self, num_adherant, nom, prenom):
        self.__num_adherant = num_adherant
        self.__nom = nom
        self.__prenom = prenom

    def get_num_adherant(self):
        return self.__num_adherant

    def get_nom_adherant(self):
        return self.__nom

    def get_prenom_adherant(self):
        return self.__prenom






